    private void accept_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accept_btnActionPerformed
        // TODO add your handling code here:
        if ((!a_txt.getText().equals("") && Integer.parseInt(a_txt.getText()) >= 0 && Integer.parseInt(a_txt.getText()) <= 9999) && (!b_txt.getText().equals("") && Integer.parseInt(b_txt.getText()) >= 0 && Integer.parseInt(b_txt.getText()) <= 9999) && (!m_txt.getText().equals("") && Integer.parseInt(m_txt.getText()) >= 0 && Integer.parseInt(m_txt.getText()) <= 9999) && (!seed_txt.getText().equals("") && Integer.parseInt(seed_txt.getText()) >= 0 && Integer.parseInt(seed_txt.getText()) <= 9999)) {
            int intervalo = 1;
            if (txt_int_inicial.getText() != null && !txt_int_inicial.getText().isEmpty() && Integer.parseInt(txt_int_inicial.getText()) >= 1)
            {
                intervalo = Integer.parseInt(txt_int_inicial.getText());
            }
            controller.calculate(Integer.parseInt(a_txt.getText()), 
                    Integer.parseInt(b_txt.getText()), 
                    Integer.parseInt(m_txt.getText()), 
                    Integer.parseInt(seed_txt.getText()), 
                    intervalo);
        } else {
            JOptionPane.showMessageDialog(null, "Error en los valores", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_accept_btnActionPerformed

